import { loadLocaleAsync } from '@/i18n/i18n-util.async'

const getI18nProps: GetStaticProps = async (context) => {
  const locale = context.locale as Locales

  await loadLocaleAsync(locale)

  return {
    props: {
      i18n: {
        locale: locale,
        dictionary: loadedLocales[locale],
      },
    },
  }
}

